package com.weijie.pojo;

public class User {
    private int bid;
    private String bName;
    private String bFir;
    private int bTfn;
    private String bPwd;

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public String getbName() {
        return bName;
    }

    public void setbName(String bName) {
        this.bName = bName;
    }

    public String getbFir() {
        return bFir;
    }

    public void setbFir(String bFir) {
        this.bFir = bFir;
    }

    public int getbTfn() {
        return bTfn;
    }

    public void setbTfn(int bTfn) {
        this.bTfn = bTfn;
    }

    public String getbPwd() {
        return bPwd;
    }

    public void setbPwd(String bPwd) {
        this.bPwd = bPwd;
    }
}
